# Full list of operators.

# == Equality
# != Inequality
# <> Inequality (deprecated)
# >  Greater than
# >= Greater than or equal to
# < Less than
# <= Less than or equal to

# Examples:
username = 'jon'

if username == 'jon':
    print('welcome')
else:
    print('not you')

if username != 'jon':
    print('good, not jon')
else:
    print("you're jon")

number = 55

if number < 100:
    print('i am smaller than 100')
else:
    print('bigger than 100')
